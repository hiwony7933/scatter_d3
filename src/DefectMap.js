var margin = { top: 20, right: 20, bottom: 30, left: 40 },
    width = 750 - margin.left - margin.right,
    height = 500 - margin.top - margin.bottom;

var graphdata = d3.range(200).map(function (d) {
    return { x: d3.randomLogNormal()(), y: d3.randomLogNormal()() };
});

var xMax = d3.max(graphdata, function (d) {
        return d["x"];
    }),
    yMax = d3.max(graphdata, function (d) {
        return d["y"];
    });

var xScale = d3.scaleLinear().range([0, width]).domain([0, xMax]).nice();
var yScale = d3.scaleLinear().range([height, 0]).domain([0, yMax]);
var xAxis = d3.axisBottom().scale(xScale);
var yAxis = d3.axisLeft().scale(yScale);
var yTicks = 5;

var svg = d3
    .select("#plotspace")
    .append("svg")
    .attr("width", width + margin.left + margin.right)
    .attr("height", height + margin.top + margin.bottom)
    .append("g")
    .attr("id", "plot")
    .attr("transform", "translate(" + margin.left + "," + margin.top + ")");

// create a clipping region
svg.append("defs").append("clipPath").attr("id", "clip").append("rect").attr("width", width).attr("height", height);

var plotArea = svg
    .append("g") // we don't want to clip the axes.
    .attr("clip-path", "url(#clip)");

var gX = svg
    .append("g")
    .attr("transform", "translate(0," + height + ")")
    .attr("class", "x axis")
    .call(xAxis);

var gY = svg.append("g").attr("transform", "translate(0,0)").attr("class", "y axis").call(yAxis);

var bubble = plotArea
    .selectAll(".bubble") // add to clipped area.
    .data(graphdata)
    .enter()
    .append("path")
    .attr("class", "bubble")
    .attr("d", d3.symbol().type(d3.symbolCircle).size(30))
    .attr("transform", function (d) {
        return "translate(" + xScale(d["x"]) + "," + yScale(d["y"]) + ")";
    })
    .attr("r", 3.5)
    .attr("fill-opacity", 0.7)
    .style("fill", "blue");

// Pan and zoom
var zoom = d3
    .zoom()
    .scaleExtent([1, 20])
    .translateExtent([
        [0, 0],
        [width, height]
    ])
    .extent([
        [0, 0],
        [width, height]
    ])
    .on("zoom", zoomed);

svg.append("rect")
    .attr("width", width)
    .attr("height", height)
    .style("fill", "none")
    .style("pointer-events", "all")
    .call(zoom);

function zoomed() {
    var new_xScale = d3.event.transform.rescaleX(xScale);
    var new_yScale = d3.event.transform.rescaleY(yScale);

    gX.call(xAxis.scale(new_xScale));
    gY.call(yAxis.scale(new_yScale));
    bubble.data(graphdata).attr("transform", function (d) {
        return "translate(" + new_xScale(d["x"]) + "," + new_yScale(d["y"]) + ")";
    });
}
